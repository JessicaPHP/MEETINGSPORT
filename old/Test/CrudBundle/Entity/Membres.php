<?php

namespace Test\CrudBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Membres
 *
 * @ORM\Table(name="membres", indexes={@ORM\Index(name="sport_favoris", columns={"idSport"})})
